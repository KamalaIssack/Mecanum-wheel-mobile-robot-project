/* USER CODE BEGIN 4 */
void motor_set(TIM_HandleTypeDef *htim, uint32_t rpwm_ch, uint32_t lpwm_ch,
               GPIO_TypeDef *en_port, uint16_t ren_pin, uint16_t len_pin,
               int16_t speed)
{
    uint16_t duty = (uint16_t)(speed < 0 ? -speed : speed);
    if (duty > 999) duty = 999;

    if (speed > 0)
    {
        __HAL_TIM_SET_COMPARE(htim, rpwm_ch, duty);
        __HAL_TIM_SET_COMPARE(htim, lpwm_ch, 0);
    }
    else if (speed < 0)
    {
        __HAL_TIM_SET_COMPARE(htim, rpwm_ch, 0);
        __HAL_TIM_SET_COMPARE(htim, lpwm_ch, duty);
    }
    else
    {
        __HAL_TIM_SET_COMPARE(htim, rpwm_ch, 0);
        __HAL_TIM_SET_COMPARE(htim, lpwm_ch, 0);
    }
}

void mecanum_drive(int16_t vx, int16_t vy, int16_t omega)
{
    int16_t fl = vx - vy - omega;
    int16_t fr = vx + vy + omega;
    int16_t rl = vx + vy - omega;
    int16_t rr = vx - vy + omega;

    motor_set(&htim3, TIM_CHANNEL_1, TIM_CHANNEL_2, GPIOC, M1_REN_Pin, M1_LEN_Pin, fl);
    motor_set(&htim3, TIM_CHANNEL_3, TIM_CHANNEL_4, GPIOC, M2_REN_Pin, M2_LEN_Pin, fr);
    motor_set(&htim4, TIM_CHANNEL_1, TIM_CHANNEL_2, GPIOC, M3_REN_Pin, M3_LEN_Pin, rl);
    motor_set(&htim4, TIM_CHANNEL_3, TIM_CHANNEL_4, GPIOC, M4_REN_Pin, M4_LEN_Pin, rr);
}
